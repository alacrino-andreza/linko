// Three.js Hero Background
function initThreeJS() {
    const canvas = document.getElementById('hero-canvas');
    const scene = new THREE.Scene();
    const camera = new THREE.PerspectiveCamera(75, window.innerWidth / window.innerHeight, 0.1, 1000);
    const renderer = new THREE.WebGLRenderer({ canvas, alpha: true, antialias: true });
    
    renderer.setSize(window.innerWidth, window.innerHeight);
    renderer.setPixelRatio(Math.min(window.devicePixelRatio, 2));

    // Create particles
    const particlesGeometry = new THREE.BufferGeometry();
    const particlesCount = 1500;
    const posArray = new Float32Array(particlesCount * 3);
    const velocityArray = new Float32Array(particlesCount);

    for(let i = 0; i < particlesCount * 3; i += 3) {
        posArray[i] = (Math.random() - 0.5) * 50;
        posArray[i+1] = (Math.random() - 0.5) * 50;
        posArray[i+2] = (Math.random() - 0.5) * 50;
        velocityArray[i/3] = Math.random() * 0.02 + 0.01;
    }

    particlesGeometry.setAttribute('position', new THREE.BufferAttribute(posArray, 3));
    
    const particlesMaterial = new THREE.PointsMaterial({
        size: 0.05,
        color: 0xFF6B00,
        transparent: true,
        opacity: 0.6,
        blending: THREE.AdditiveBlending
    });

    const particlesMesh = new THREE.Points(particlesGeometry, particlesMaterial);
    scene.add(particlesMesh);

    // Add lines
    const linesGeometry = new THREE.BufferGeometry();
    const linesCount = 500;
    const linesPosArray = new Float32Array(linesCount * 3);
    
    for(let i = 0; i < linesCount * 3; i += 3) {
        linesPosArray[i] = (Math.random() - 0.5) * 30;
        linesPosArray[i+1] = (Math.random() - 0.5) * 30;
        linesPosArray[i+2] = (Math.random() - 0.5) * 10;
    }
    
    linesGeometry.setAttribute('position', new THREE.BufferAttribute(linesPosArray, 3));
    
    const linesMaterial = new THREE.PointsMaterial({
        size: 0.02,
        color: 0xffffff,
        transparent: true,
        opacity: 0.3
    });
    
    const linesMesh = new THREE.Points(linesGeometry, linesMaterial);
    scene.add(linesMesh);

    camera.position.z = 5;

    let mouseX = 0;
    let mouseY = 0;

    document.addEventListener('mousemove', (event) => {
        mouseX = (event.clientX / window.innerWidth) * 2 - 1;
        mouseY = -(event.clientY / window.innerHeight) * 2 + 1;
    });

    function animate() {
        requestAnimationFrame(animate);

        particlesMesh.rotation.y += 0.001;
        particlesMesh.rotation.x += 0.0005;
        
        linesMesh.rotation.y -= 0.002;

        // Mouse interaction
        particlesMesh.rotation.x += mouseY * 0.01;
        particlesMesh.rotation.y += mouseX * 0.01;

        renderer.render(scene, camera);
    }

    animate();

    window.addEventListener('resize', () => {
        camera.aspect = window.innerWidth / window.innerHeight;
        camera.updateProjectionMatrix();
        renderer.setSize(window.innerWidth, window.innerHeight);
    });
}

// Main Animations
function initAnimations() {
    // Hero Text Reveal
    const tl = gsap.timeline();
    
    tl.to('#hero-subtitle', {
        y: 0,
        opacity: 1,
        duration: 0.8,
        ease: 'power4.out'
    })
    .to('#hero-title .reveal-text span', {
        y: 0,
        opacity: 1,
        duration: 1,
        stagger: 0.2,
        ease: 'power4.out'
    }, '-=0.4')
    .to('#hero-desc', {
        y: 0,
        opacity: 1,
        duration: 0.8,
        ease: 'power4.out'
    }, '-=0.6');

    // Speed Lines Animation
    gsap.to('.speed-line', {
        opacity: 0.5,
        x: '100%',
        duration: 2,
        stagger: 0.3,
        repeat: -1,
        ease: 'none'
    });

    // Stats Counter Animation
    const stats = document.querySelectorAll('.stat-number');
    stats.forEach(stat => {
        const target = parseInt(stat.getAttribute('data-target'));
        
        ScrollTrigger.create({
            trigger: stat,
            start: 'top 80%',
            onEnter: () => {
                gsap.to(stat, {
                    innerHTML: target,
                    duration: 2,
                    snap: { innerHTML: 1 },
                    ease: 'power2.out',
                    onUpdate: function() {
                        if (target === 5000) {
                            stat.innerHTML = '+' + Math.round(this.targets()[0].innerHTML).toLocaleString();
                        }
                    }
                });
            }
        });
    });

    // Project Cards Animation
    gsap.utils.toArray('.project-card').forEach((card, i) => {
        gsap.from(card, {
            scrollTrigger: {
                trigger: card,
                start: 'top 85%',
            },
            y: 100,
            opacity: 0,
            duration: 0.8,
            delay: i * 0.1,
            ease: 'power4.out'
        });
    });

    // Services Animation
    gsap.utils.toArray('.service-card').forEach((item, i) => {
        gsap.from(item, {
            scrollTrigger: {
                trigger: item,
                start: 'top 85%',
            },
            y: 50,
            opacity: 0,
            duration: 0.6,
            delay: i * 0.1,
            ease: 'power3.out'
        });
    });

    // Parallax on scroll
    gsap.to('#hero-title', {
        scrollTrigger: {
            trigger: 'body',
            start: 'top top',
            end: 'bottom top',
            scrub: 1
        },
        y: 200,
        opacity: 0
    });
}

